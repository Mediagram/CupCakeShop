
package model;

public class User 
{
    private int id;
    private String email;
    private String password;
    private String name;

    public int getId()
    {
        return id;
    }

    public String getEmail()
    {
        return email;
    }

    public String getPassword()
    {
        return password;
    }

    public String getName()
    {
        return name;
    }
}
